#include <stdio.h>

int main()
{
    int a[5][5];

    printf("Enter row 1:");
    scanf("%d %d %d %d %d",&a[0][0],&a[0][1],&a[0][2],&a[0][3],&a[0][4]);
    printf("Enter row 2:");
    scanf("%d %d %d %d %d",&a[1][0],&a[1][1],&a[1][2],&a[1][3],&a[1][4]);
    printf("Enter row 3:");
    scanf("%d %d %d %d %d",&a[2][0],&a[2][1],&a[2][2],&a[2][3],&a[2][4]);
    printf("Enter row 4:");
    scanf("%d %d %d %d %d",&a[3][0],&a[3][1],&a[3][2],&a[3][3],&a[3][4]);
    printf("Enter row 5:");
    scanf("%d %d %d %d %d",&a[4][0],&a[4][1],&a[4][2],&a[4][3],&a[4][4]);

    printf("Row totals: %d %d %d %d %d\n",a[0][0]+a[0][1]+a[0][2]+a[0][3]+a[0][4],
                                        a[1][0]+a[1][1]+a[1][2]+a[1][3]+a[1][4],
                                        a[2][0]+a[2][1]+a[2][2]+a[2][3]+a[2][4],
                                        a[3][0]+a[3][1]+a[3][2]+a[3][3]+a[3][4],
                                        a[4][0]+a[4][1]+a[4][2]+a[4][3]+a[4][4]);

    printf("Column totals: %d %d %d %d %d",a[0][0]+a[1][0]+a[2][0]+a[3][0]+a[4][0],
                                           a[0][1]+a[1][1]+a[2][1]+a[3][1]+a[4][1],
                                           a[0][2]+a[1][2]+a[2][2]+a[3][2]+a[4][2],
                                           a[0][3]+a[1][3]+a[2][3]+a[3][3]+a[4][3],
                                           a[0][4]+a[1][4]+a[2][4]+a[3][4]+a[4][4]);

    return 0;
}